/**
 * Builds responsive WebP tiers from public/product-slide-*.png and writes src/generated/gallery-meta.ts.
 *
 * - Hero tier (stem.webp): Lanczos resize to max HERO_MAX_PX width — fast loads on mobile / narrow layouts.
 * - Full tier (stem@2x.webp): native pixel dimensions when source is wider than hero — Retina / wide screens / zoom.
 *
 * Replace PNGs in public/ with full-resolution masters (e.g. 5464×8192), then run: npm run optimize-gallery
 * Free tools for prep: https://squoosh.app (Web), GIMP, ImageMagick (`magick in.png -resize 50% out.png`).
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import sharp from "sharp";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const pub = path.join(root, "public");
const genDir = path.join(root, "src", "generated");
const stems = ["product-slide-01", "product-slide-02", "product-slide-03"];

/** Max width for hero WebP; full native tier added when PNG is wider than this. */
const HERO_MAX_PX = 2800;

const meta = [];

for (const stem of stems) {
  const input = path.join(pub, `${stem}.png`);
  if (!fs.existsSync(input)) {
    throw new Error(`[optimize-product-gallery] missing required ${path.basename(input)}`);
  }

  const im = sharp(input);
  const info = await im.metadata();
  const w = info.width ?? 1;
  const h = info.height ?? 1;

  const heroW = Math.min(HERO_MAX_PX, w);
  const heroPath = path.join(pub, `${stem}.webp`);

  await sharp(input)
    .resize(heroW, null, {
      fit: "inside",
      withoutEnlargement: true,
      kernel: sharp.kernel.lanczos3,
    })
    .webp({ quality: 95, effort: 6, smartSubsample: true })
    .toFile(heroPath);

  const fullPath = path.join(pub, `${stem}@2x.webp`);
  let fullWebpWidth = heroW;
  let hasTwoWebpTiers = false;

  if (w > heroW) {
    hasTwoWebpTiers = true;
    fullWebpWidth = w;
    await sharp(input)
      .webp({ quality: 92, effort: 6, smartSubsample: true })
      .toFile(fullPath);
  } else {
    if (fs.existsSync(fullPath)) fs.unlinkSync(fullPath);
  }

  meta.push({
    stem,
    intrinsicWidth: w,
    intrinsicHeight: h,
    heroWebpWidth: heroW,
    fullWebpWidth,
    hasTwoWebpTiers,
  });
}

fs.mkdirSync(genDir, { recursive: true });
const ts = `/* Generated by scripts/optimize-product-gallery.mjs — do not edit by hand */
export type GallerySlideMeta = {
  stem: string;
  intrinsicWidth: number;
  intrinsicHeight: number;
  heroWebpWidth: number;
  fullWebpWidth: number;
  hasTwoWebpTiers: boolean;
};

export const GALLERY_SLIDE_META: readonly GallerySlideMeta[] = ${JSON.stringify(meta, null, 2)} as const;
`;
fs.writeFileSync(path.join(genDir, "gallery-meta.ts"), ts, "utf8");

console.log("[optimize-product-gallery] WebP + src/generated/gallery-meta.ts for", meta.length, "slides");
