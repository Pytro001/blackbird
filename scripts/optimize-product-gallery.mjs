/**
 * Builds responsive WebP tiers from slide masters in public/ and writes src/generated/gallery-meta.ts.
 *
 * Sources (first match wins): stem.jpg | stem.jpeg | stem.png | stem.webp
 *
 * - Hero tier (stem.webp): Lanczos resize to max HERO_MAX_PX width.
 * - Full tier (stem@2x.webp): native pixels when source is wider than hero max.
 *
 * After replacing masters: npm run optimize-gallery
 * Free tools: https://squoosh.app, GIMP, ImageMagick.
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import sharp from "sharp";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const pub = path.join(root, "public");
const genDir = path.join(root, "src", "generated");
const stems = ["product-slide-01", "product-slide-02", "product-slide-03"];

const SOURCE_EXTS = ["jpg", "jpeg", "png", "webp"];

/** Max width for hero WebP; full native tier added when source is wider than this. */
const HERO_MAX_PX = 2800;

/** WebP encode for full-resolution tier (native pixels when source > hero max). */
const FULL_WEBP_QUALITY_DEFAULT = 92;
const FULL_WEBP_QUALITY_HIGH = 96;

/** Stems that get the higher full-tier quality (large marketing masters). */
const FULL_WEBP_QUALITY_HIGH_STEMS = new Set(["product-slide-01"]);

/**
 * @param {string} pubDir
 * @param {string} stem
 * @returns {{ path: string, fallbackExt: string }}
 */
function resolveSlideSource(pubDir, stem) {
  for (const ext of SOURCE_EXTS) {
    const p = path.join(pubDir, `${stem}.${ext}`);
    if (fs.existsSync(p)) return { path: p, fallbackExt: ext };
  }
  throw new Error(
    `[optimize-product-gallery] missing source for ${stem} (${SOURCE_EXTS.map((e) => `${stem}.${e}`).join(" | ")})`,
  );
}

const meta = [];

for (const stem of stems) {
  const { path: input, fallbackExt } = resolveSlideSource(pub, stem);

  const info = await sharp(input).metadata();
  const w = info.width ?? 1;
  const h = info.height ?? 1;

  const heroW = Math.min(HERO_MAX_PX, w);
  const heroPath = path.join(pub, `${stem}.webp`);

  await sharp(input)
    .resize(heroW, null, {
      fit: "inside",
      withoutEnlargement: true,
      kernel: sharp.kernel.lanczos3,
    })
    .webp({ quality: 95, effort: 6, smartSubsample: true })
    .toFile(heroPath);

  const fullPath = path.join(pub, `${stem}@2x.webp`);
  let fullWebpWidth = heroW;
  let hasTwoWebpTiers = false;

  if (w > heroW) {
    hasTwoWebpTiers = true;
    fullWebpWidth = w;
    const fullQuality = FULL_WEBP_QUALITY_HIGH_STEMS.has(stem)
      ? FULL_WEBP_QUALITY_HIGH
      : FULL_WEBP_QUALITY_DEFAULT;
    await sharp(input)
      .webp({ quality: fullQuality, effort: 6, smartSubsample: true })
      .toFile(fullPath);
  } else {
    if (fs.existsSync(fullPath)) fs.unlinkSync(fullPath);
  }

  meta.push({
    stem,
    fallbackExt,
    intrinsicWidth: w,
    intrinsicHeight: h,
    heroWebpWidth: heroW,
    fullWebpWidth,
    hasTwoWebpTiers,
  });
}

fs.mkdirSync(genDir, { recursive: true });
const ts = `/* Generated by scripts/optimize-product-gallery.mjs — do not edit by hand */
export type GallerySlideMeta = {
  stem: string;
  /** Raster fallback for <img> (\`stem.ext\`) — jpg, jpeg, png, or webp */
  fallbackExt: string;
  intrinsicWidth: number;
  intrinsicHeight: number;
  heroWebpWidth: number;
  fullWebpWidth: number;
  hasTwoWebpTiers: boolean;
};

export const GALLERY_SLIDE_META: readonly GallerySlideMeta[] = ${JSON.stringify(meta, null, 2)} as const;
`;
fs.writeFileSync(path.join(genDir, "gallery-meta.ts"), ts, "utf8");

console.log("[optimize-product-gallery] WebP + src/generated/gallery-meta.ts for", meta.length, "slides");
